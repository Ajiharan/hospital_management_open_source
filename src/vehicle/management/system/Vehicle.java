    public boolean CheckEmpty(){
        String VNo = txt_RegNo.getText();
        String ePattern = "^[A-Z]{2}[ -][A-Z]{3}[-][0-9]{4}$";
        java.util.regex.Pattern p = java.util.regex.Pattern.compile(ePattern);
        java.util.regex.Matcher m = p.matcher(VNo);
        
        if(txt_RegNo.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Registration number Field is empty");
            return false;
        }
         else if(!m.matches()){
            JOptionPane.showMessageDialog(null,"Please check characters");
            return false;
        }
        else if(txt_Brand.getSelectedIndex() == -1){
            JOptionPane.showMessageDialog(null,"Select Brand Name"); 
            return false;
        }
        else if(txt_Model.getSelectedIndex() == -1){
            JOptionPane.showMessageDialog(null,"Select Brand Name"); 
            return false;
        }
        else if(((JTextField)txt_Date.getDateEditor().getUiComponent()).getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Date Field is empty");     
            return false;
        }
        else if(txt_Fueltype.getSelectedIndex() == -1){
            JOptionPane.showMessageDialog(null,"Select Fuel type"); 
            return false;
        }
        else if(txt_ChassisNo.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"ChassisNo Field is empty"); 
            return false;
        }
        else if(txt_Status.getSelectedIndex() == -1){
            JOptionPane.showMessageDialog(null,"Select Status"); 
            return false;
        }
        else{
            return true;
        }
    }
    
